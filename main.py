shopping_list = {'Яблука':10}

def create(file, shopping_list):
    with open(file, 'w', encoding="utf-8") as f:
        for product, count in shopping_list.items():
            f.write(f'{product} - {count}\n')

def add():
    global shopping_list
    product = input('Введіть який продукт бажаєте купити: ')
    count = int(input('Введіть у якій кількості вам необхідно купити це: '))
    shopping_list[product] = count
    print('Ви додали новий продукт!')

def read_file(file):
    with open(file, 'r', encoding="utf-8") as f:
        print(f.read())

